# Largest product in a series
def product_arr(pro, l):
    y = 0
    k = -1
    j = 12
    z = 1
    switch = True
    while switch:
        i = pro[y]
        i = int(i)
        z = i * z
        print(z, "#", i)
        if y == j:
            k += 1
            y = k  # resets y
            z = 1
            j += 1
        if j == l:
            switch = False
        y += 1


